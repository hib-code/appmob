  /// Data text style using JetBrains Mono for phone numbers, addresses, and pricing
  static TextStyle dataTextStyle({
    required bool isLight,
    double fontSize = 14,
    FontWeight fontWeight = FontWeight.w400,
  }) {
    return GoogleFonts.jetBrainsMono(
      fontSize: fontSize,
      fontWeight: fontWeight,
      color: isLight ? textPrimaryLight : textPrimaryDark,
      letterSpacing: 0,
      height: 1.4,
    );
  }

  /// Helper method to get semantic colors
  static Color getSemanticColor(String type, {required bool isLight}) {
    switch (type.toLowerCase()) {
      case 'success':
        return isLight ? accentLight : accentDark;
      case 'warning':
        return isLight ? warningLight : warningDark;
      case 'error':
        return isLight ? errorLight : errorDark;
      case 'info':
        return isLight ? primaryLight : primaryDark;
      default:
        return isLight ? textSecondaryLight : textSecondaryDark;
    }
  }
